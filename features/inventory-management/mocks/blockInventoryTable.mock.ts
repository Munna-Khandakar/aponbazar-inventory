const createProducts = (
  categoryId: string,
  categoryName: string,
  stockIn: number,
  stockOut: number,
  lifting5d: number,
  days: number,
  pct: number,
  valIn: number,
  supplier: string,
  lead: number
): InventoryBlockTableItem[] =>
  ["500g Pack", "1kg Bag", "2kg Box", "Family Pack"].map((packaging, index) => ({
    id: `${categoryId}-${index + 1}`,
    name: `${categoryName} - ${packaging}`,
    code: `SKU-${String(1000 + index * 17 + categoryName.length).padStart(4, "0")}`,
    stockIn: Math.round(stockIn * (0.18 + index * 0.09)),
    stockOut: Math.round(stockOut * (0.18 + index * 0.09)),
    lifting5d: Math.round(lifting5d * (0.18 + index * 0.09)),
    days: Math.max(0, Math.round(days * (0.5 + index * 0.2))),
    pct: Math.max(2, Math.round(pct * (0.7 + index * 0.1))),
    valIn: Math.round(valIn * (0.2 + index * 0.1)),
    supplier,
    lead,
  }))

